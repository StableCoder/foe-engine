typedef enum foeBringupResult {
    FOE_SKUNKWORKS_SUCCESS = 0,

    FOE_SKUNKWORKS_ERROR_OUT_OF_MEMORY,
    FOE_SKUNKWORKS_INITIALIZATION_FAILED,
    FOE_SKUNKWORKS_NOT_INITIALIZED,
    FOE_SKUNKWORKS_FAILED_TO_LOAD_PLUGIN,

    FOE_SKUNKWORKS_ERROR_NO_TIMELINE_SEMAPHORE_SUPPORT,

    // Loaders
    FOE_SKUNKWORKS_ERROR_LOADER_INITIALIZATION_FAILED,
    FOE_SKUNKWORKS_ERROR_INCOMPATIBLE_CREATE_INFO,
    FOE_SKUNKWORKS_ERROR_INCOMPATIBLE_RESOURCE,
    FOE_SKUNKWORKS_ERROR_IMPORT_FAILED,

    FOE_SKUNKWORKS_ERROR_FAILED_TO_REGISTER_EXPORTERS,

    FOE_SKUNKWORKS_ERROR_NO_PHYSICAL_DEVICE_MEETS_REQUIREMENTS,
    // RenderGraph - RenderScene
    FOE_SKUNKWORKS_RENDER_SCENE_COLOUR_TARGET_NOT_IMAGE,
    FOE_SKUNKWORKS_RENDER_SCENE_COLOUR_TARGET_NOT_MUTABLE,
    FOE_SKUNKWORKS_RENDER_SCENE_COLOUR_TARGET_NO_STATE,
    FOE_SKUNKWORKS_RENDER_SCENE_DEPTH_TARGET_NOT_IMAGE,
    FOE_SKUNKWORKS_RENDER_SCENE_DEPTH_TARGET_NOT_MUTABLE,
    FOE_SKUNKWORKS_RENDER_SCENE_DEPTH_TARGET_NO_STATE,
    // Armature System
    FOE_SKUNKWORKS_ERROR_NO_ARMATURE_POOL_PROVIDED,
    FOE_SKUNKWORKS_ERROR_NO_ARMATURE_STATE_POOL_PROVIDED,

    // WSI
    FOE_SKUNKWORKS_ERROR_UNABLE_TO_CREATE_SURFACE,
    FOE_SKUNKWORKS_ERROR_INCOMPATIBLE_GRAPHICS_SESSION,

    // Need to have a negative enum value to prevent treatment as a flag
    FOE_SKUNKWORKS_ERROR_NEGATIVE_VALUE = FOE_RESULT_MIN_ENUM,
} foeBringupResult;

void foeBringupResultToString(foeBringupResult value, char buffer[FOE_MAX_RESULT_STRING_SIZE]);

inline foeResultSet to_foeResult(foeBringupResult value) {
    foeResultSet result = {
        .value = value,
        .toString = (PFN_foeResultToString)foeBringupResultToString,
    };

    return result;
}

